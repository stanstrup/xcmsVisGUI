suppressWarnings(suppressMessages(source("global.R")))
suppressMessages(library(ggplot2))
df <- data.frame(x = 1:10, y = (1:10)^2, g = "a")
gg <- ggplot(df, aes(x, y, color = g, text = paste(x))) + geom_line()
p1 <- ggplotly(gg, tooltip = "text", dynamicTicks = TRUE) %>% layout(uirevision = "tic")
p2 <- ggplotly(gg, tooltip = "text", dynamicTicks = FALSE) %>% layout(uirevision = "tic")
b1 <- plotly::plotly_build(p1); b2 <- plotly::plotly_build(p2)
cat("dynamicTicks=TRUE  uirevision:", b1$x$layout$uirevision %||% "NULL", "\n")
cat("dynamicTicks=FALSE uirevision:", b2$x$layout$uirevision %||% "NULL", "\n")
cat("xaxis autorange (dynTRUE):", b1$x$layout$xaxis$autorange %||% "unset", "\n")
cat("ZOOM TEST OK\n")
